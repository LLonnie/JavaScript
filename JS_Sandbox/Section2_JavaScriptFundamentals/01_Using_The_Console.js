// Logging to console from application.
  // Logging a String
  console.log('Hello World');

  // Logging a Number
  console.log(5);

  // Logging a Boolean
  console.log(true);

  // Logging a Variable
  var greeting = 'Hello, how are you?';
  console.log(greeting);

  // Logging an Array
  console.log([1, 2, 3, 4]);

  // Logging an Object
  console.log({a:1, b:2, c:3});
  // Console log an Object as table.
  console.table({a:1, b:2, c:3});

  // Logging an Error.
  console.error('This is an error');
  
  // Clear the console.
  console.clear();

  // Logging a Warning.
  console.warn('This is a warning');

  // Console timer
  console.time('Hello');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
  console.timeEnd('Hello');
